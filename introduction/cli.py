def _argv_contains(argv: Iterable[str], hints: set[str]) -> bool:
    """Return ``True`` when any CLI token matches a known workflow option."""
    for token in argv:
        name = token.split("=", 1)[0]
        if name in hints:
            return True
    return False


def _route_legacy_no_subcommand(argv: list[str]) -> str | None:
    """Route compatibility no-subcommand usage to a workflow when possible.

    Ambiguous flags are intentionally resolved conservatively:
    - kinematics wins when ``--op`` or vector/function flags are present.
    - mobius wins when rendering geometry flags are present.
    - manipulator wins when manipulator enumeration flags are present.
    """
    if _argv_contains(argv, _KINEMATICS_HINTS):
        return "kinematics"
    if _argv_contains(argv, _MOBIUS_HINTS):
        return "mobius"
    if _argv_contains(argv, _MANIPULATOR_HINTS):
        return "manipulator"
    return None


def _strip_separator(args: list[str]) -> list[str]:
    """Remove a leading ``--`` separator from delegated argument lists."""
    if args and args[0] == "--":
        return args[1:]
    return args


def main(argv: list[str] | None = None) -> int:
    """Run the package-level ``introduction`` command-line interface."""
    raw_argv = list(sys.argv[1:] if argv is None else argv)

    # Historical compatibility: allow users to call the package-level CLI with
    # the original script options and route them to the most likely workflow.
    if raw_argv and raw_argv[0] not in {
        "sphinx-skel",
        "manipulator",
        "mobius",
        "kinematics",
        "-h",
        "--help",
    }:
        route = _route_legacy_no_subcommand(raw_argv)
        if route is None:
            parser = build_parser()
            parser.error(
                "Could not infer a workflow from no-subcommand arguments. "
                "Use one of: manipulator, mobius, kinematics, sphinx-skel."
            )
        raw_argv = [route, *raw_argv]

    parser = build_parser()
    args = parser.parse_args(raw_argv)

    if args.cmd == "sphinx-skel":
        return _run_sphinx_skel(args)

    if args.cmd == "manipulator":
        return _run_module_main("introduction.manipulator", _strip_separator(args.workflow_args))

    if args.cmd == "mobius":
        return _run_module_main("introduction.mobius", _strip_separator(args.workflow_args))

    if args.cmd == "kinematics":
        return _run_module_main("introduction.kinematics", _strip_separator(args.workflow_args))

    parser.print_help()
    return 0


if __name__ == "__main__":  # pragma: no cover
    raise SystemExit(main())
